package cn.whyx.pojo;

/**
 * 角色实体类
 */
public class Role {
    private Integer id;         //角色ID
    private String name;       //角色名称

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
